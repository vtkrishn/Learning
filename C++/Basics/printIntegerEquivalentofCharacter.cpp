#include <iostream>

int main(){

  std::cout << "ASCII equivalent of 'A'" << static_cast <int  >('A') << std::endl;
  std::cout << "ASCII equivalent of 'b'" << static_cast <int  >('b') << std::endl;
  std::cout << "ASCII equivalent of '0'" << static_cast <int  >('0') << std::endl;
  std::cout << "ASCII equivalent of '$'" << static_cast <int  >('$') << std::endl;
  return 0;
}
